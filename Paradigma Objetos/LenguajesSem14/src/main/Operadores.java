package main;

/**
 *
 * @author jjuli
 */
public class Operadores {

    private final String suma;
    private final String resta;
    private final String mult;
    private final String div;

    public Operadores() {
        this.suma = "+";
        this.resta = "-";
        this.mult = "*";
        this.div = "/";
    }

    public String getSuma() {
        return suma;
    }

    public String getResta() {
        return resta;
    }

    public String getMult() {
        return mult;
    }

    public String getDiv() {
        return div;
    }

}
